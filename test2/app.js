//การเขียนโปรแกรมจาก pseudocode มีการวนลูปที่ค่า x เพิ่มขึ้นเป็นสองเท่าและค่า i เพิ่มขึ้นทีละหนึ่งจนกว่า x จะมีค่าเท่ากับ 1000 การเขียนโปรแกรมใน Node.js จะใช้ฟังก์ชัน while ในการวนลูป

let x = 1;
let i = 1;

while (x < 1000) {
    x *= 2;
    i += 1;
}

console.log(`ค่า x สุดท้าย: ${x}, ค่า i: ${i}`);

//ผลลัพธ์คือ ค่า x สุดท้าย: 1024, ค่า i: 11
